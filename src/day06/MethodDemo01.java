package src.day06;

/**
 * 無參無訪回值方法
 */
public class MethodDemo01 {
    public static void main(String[] args) {
        oneDay();
        oneDay();
        oneDay();

    }


    static void oneDay(){
        System.out.println("早餐");
        System.out.println("午餐");
        System.out.println("晚餐");
        System.out.println("整備");
        System.out.println("睡覺");
    }
}

